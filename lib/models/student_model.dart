class Student {
  final String name;
  final String rollNo;
  final String bloodGroup;
  final String fathersName;
  final String mothersName;
  final String fathersPhoneNo;
  final String mothersPhoneNo;
  final bool gender;
  final DateTime dob;
  final String address;

  Student(
      {this.name,
      this.rollNo,
      this.bloodGroup,
      this.fathersName,
      this.mothersName,
      this.fathersPhoneNo,
      this.mothersPhoneNo,
      this.gender,
      this.dob,
      this.address});
}
